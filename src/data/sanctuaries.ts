export interface Sanctuary {
  name: string;
  type: "NP" | "WLS" | "TR" | "BS" | "CR" | "Other";
  wikipediaUrl: string | null;
  ngo?: string;
  ngoUrl?: string;
}

export interface StateData {
  state: string;
  sanctuaries: Sanctuary[];
}

function inferType(name: string): Sanctuary["type"] {
  const n = name.toLowerCase();
  if (n.includes("national park") || n.endsWith(" np")) return "NP";
  if (n.includes("tiger reserve") || n.endsWith(" tr")) return "TR";
  if (n.includes("bird sanctuary") || n.endsWith(" bs")) return "BS";
  if (n.includes("conservation reserve") || n.endsWith(" cr")) return "CR";
  if (n.includes("wildlife sanctuary") || n.endsWith(" wls")) return "WLS";
  return "WLS";
}

function s(name: string, url: string | null, ngo?: string, ngoUrl?: string): Sanctuary {
  return { name, type: inferType(name), wikipediaUrl: url, ...(ngo ? { ngo } : {}), ...(ngoUrl ? { ngoUrl } : {}) };
}

